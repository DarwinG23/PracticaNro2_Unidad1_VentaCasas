    public Boolean verificar(){
        return (!txtComision.getText().trim().isEmpty()
                && !txtDetalle.getText().trim().isEmpty()
                 && !txtCosto.getText().trim().isEmpty()
                );
    }
    
    private void cargarTabla(){
       mtv.setVentas(ventaControl.getListC());
       tbVenta.setModel(mtv);
       tbVenta.updateUI();
    }
    
    private void cargarTablaFiltrada(String cedula) {
        mtv.setVentas(ventaControl.getListC());
        tbVenta.setModel(mtv);
        TableRowSorter<ModeloTablaVenta> sorter = new TableRowSorter<>(mtv);
        tbVenta.setRowSorter(sorter);
        RowFilter<Object, Object> filtro = RowFilter.regexFilter(cedula, 5); 
        sorter.setRowFilter(filtro);
        tbVenta.updateUI();
    }

    
    private void guardar() throws EmptyException{
        if (verificar()) {
            ventaControl.getVenta().setDetalle(txtDetalle.getText());
            ventaControl.getVenta().setPrecio(txtCosto.getText());
            ventaControl.getVenta().setComision(txtComision.getText());
            ventaControl.getVenta().setId_vendedor(UtilVistaVendedor.obtenerVendedor(cbxVendedor).getCedula());
            ventaControl.getVenta().setNum_Casa(UtilVistaCasa.obtenerCasa(cbxCasa).getNum_Casa());
            if (ventaControl.persist()) {
                JOptionPane.showMessageDialog(null, "Datos guardados");
                cargarTabla();
                limpiar();
                ventaControl.setVenta(null);
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo guardar, hubo un error");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Falta llenar campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void limpiar(){
        txtComision.setText("");
        txtCosto.setText("");
        txtDetalle.setText("");
        cbxVendedor.setSelectedIndex(0);
        cargarTabla();
        ventaControl.setVenta(null);
    }

    
    
