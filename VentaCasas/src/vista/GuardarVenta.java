    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int filaSeleccionada = tbVenta.getSelectedRow();
        if (verificar()) {
            ventaControl.getVenta().setDetalle(txtDetalle.getText());
            ventaControl.getVenta().setPrecio(txtCosto.getText());
            ventaControl.getVenta().setComision(txtComision.getText());
            ventaControl.getVenta().setId_vendedor(UtilVistaVendedor.obtenerVendedor(cbxVendedor).getCedula());
            ventaControl.getVenta().setNum_Casa(UtilVistaCasa.obtenerCasa(cbxCasa).getNum_Casa());
            if (ventaControl.marge(ventaControl.getVenta(),filaSeleccionada)) {
                JOptionPane.showMessageDialog(null, "Datos guardados");
                cargarTabla();
                limpiar();
                ventaControl.setVenta(null);
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo actualizar la venta");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Falta llenar campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        int filaSeleccionada = tbVenta.getSelectedRow();
        if (filaSeleccionada != -1) { 
            txtComision.setText(tbVenta.getValueAt(filaSeleccionada, 2).toString());
            txtCosto.setText(tbVenta.getValueAt(filaSeleccionada, 1).toString());
            txtDetalle.setText(tbVenta.getValueAt(filaSeleccionada, 4).toString());
        } else {
        JOptionPane.showMessageDialog(null, "No ha seleccionado una venta", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnCargarActionPerformed
    }
